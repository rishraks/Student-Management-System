    private void t1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t1KeyReleased
        String n=t1.getText();
        String nm=n.toUpperCase();
        if(n.equals(nm))
        {
            t1.setText(n);
        }
        else{
            JOptionPane.showMessageDialog(this,"PLEASE ENTER IN BLOCK LETTERS","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_t1KeyReleased

    private void t2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t2KeyReleased
        String id=t2.getText();
        String ed=id.toLowerCase();
        if(id.equals(ed))
        {
            t2.setText(id);
        }
        else{
            JOptionPane.showMessageDialog(this,"PLEASE ENTER IN SMALL LETTERS","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_t2KeyReleased

