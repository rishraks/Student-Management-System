           Class.forName("java.sql.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/student","root","");
           Statement st =con.createStatement();
           String q="Select * from login where name=? and email=? and phone_no=? and password=?";
           PreparedStatement pst=con.prepareStatement(q);
           pst.setString(1,n);
           pst.setString(2,id );
           pst.setLong(3,pn);
           pst.setString(4,pw);
           ResultSet rs=pst.executeQuery();
           if(rs.next())
           {
                JOptionPane.showMessageDialog(null,"You have Loged in successfully");
                this.dispose();
                Menu_page mn=new Menu_page();
                mn.setVisible(true);
                }
           else
           {
                 JOptionPane.showMessageDialog(this,"Invalid Username or password","Warning",JOptionPane.WARNING_MESSAGE);
           }
           con.close();
